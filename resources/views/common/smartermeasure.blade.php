    <li>Self-Management</li>
    <li>Communication with Instructor</li>
    <li>Interaction with Peers</li>
    <li>Learning Preference - Text</li>
    <li>Learning Preference - Visual</li>
    <li>Learning Preference - Auditory</li>
    <li>Technology Skills</li>
    <li>Classroom Website</li>
</ol>

<p>
